export const GLYPH5_W = 5;
export const GLYPH5_H = 7;
export const KERN5 = 1;
export const LINE5_H = GLYPH5_H + 2; // 9px line height

export function drawText5(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color: string,
): number {
  ctx.fillStyle = color;
  const up = text.toUpperCase();
  let cx = x;
  for (const ch of up) {
    const g = G[ch] ?? G[' '];
    for (let row = 0; row < GLYPH5_H; row++) {
      const bits = g[row];
      for (let col = 0; col < GLYPH5_W; col++) {
        if (bits & (1 << (GLYPH5_W - 1 - col))) {
          ctx.fillRect(cx + col, y + row, 1, 1);
        }
      }
    }
    cx += GLYPH5_W + KERN5;
  }
  return cx - KERN5;
}

export function textWidth5(text: string): number {
  if (!text.length) return 0;
  return text.length * (GLYPH5_W + KERN5) - KERN5;
}
